	function run($module = '', $group = ''){
		(is_object($module)) AND $this->CI = &$module;
		return parent::run($group);
	}

	public function validMobile($row) {
		if ($row == '') {
			$this->set_message('mobile', "The %s field is required.");

			return FALSE;
		} elseif (substr($row, 0, 4) !== '9665') {
			$this->set_message('validMobile', t('mobileStart', t('mobile')));

			return FALSE;
		} elseif (strlen($row) !== 12) {
			$this->set_message('validMobile', t('mobileLook', t('mobile')));

			return FALSE;
		} else {
			return TRUE;
		}
	}

	/**
	 * Alpha
	 *
	 * @access	public
	 * @param	string
	 * @return	bool
	 */
	public function alpha_space($str)
	{
		return ( ! preg_match("/^([a-z ])+$/i", $str)) ? FALSE : TRUE;
	}

